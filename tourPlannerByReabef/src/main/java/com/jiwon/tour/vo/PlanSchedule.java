package com.jiwon.tour.vo;

public class PlanSchedule {
	private int ptIdx;
	private int psIdx;
	private int psDate;
	private String psPlace;
	private String psTrans;
	private String pstime;
	private String psSchedule;
	private String psRemark;
	
	public int getPtIdx() {
		return ptIdx;
	}
	public void setPtIdx(int ptIdx) {
		this.ptIdx = ptIdx;
	}
	public int getPsIdx() {
		return psIdx;
	}
	public void setPsIdx(int psIdx) {
		this.psIdx = psIdx;
	}
	public int getPsDate() {
		return psDate;
	}
	public void setPsDate(int psDate) {
		this.psDate = psDate;
	}
	public String getPsPlace() {
		return psPlace;
	}
	public void setPsPlace(String psPlace) {
		this.psPlace = psPlace;
	}
	public String getPsTrans() {
		return psTrans;
	}
	public void setPsTrans(String psTrans) {
		this.psTrans = psTrans;
	}
	public String getPstime() {
		return pstime;
	}
	public void setPstime(String pstime) {
		this.pstime = pstime;
	}
	public String getPsSchedule() {
		return psSchedule;
	}
	public void setPsSchedule(String psSchedule) {
		this.psSchedule = psSchedule;
	}
	public String getPsRemark() {
		return psRemark;
	}
	public void setPsRemark(String psRemark) {
		this.psRemark = psRemark;
	}
	
	
}
