package com.jiwon.tour.vo;

public class PlanRequireArticle {
	private int praIdx;
	private int ptIdx;
	private String mid;
	private String praRequireArticle;
	private String praRemark;
	
	public int getPtIdx() {
		return ptIdx;
	}
	public void setPtIdx(int ptIdx) {
		this.ptIdx = ptIdx;
	}
	public int getPraIdx() {
		return praIdx;
	}
	public void setPraIdx(int praIdx) {
		this.praIdx = praIdx;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getPraRequireArticle() {
		return praRequireArticle;
	}
	public void setPraRequireArticle(String praRequireArticle) {
		this.praRequireArticle = praRequireArticle;
	}
	public String getPraRemark() {
		return praRemark;
	}
	public void setPraRemark(String praRemark) {
		this.praRemark = praRemark;
	}
	
	
}
